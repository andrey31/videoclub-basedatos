
package com.videoclub.videoclub.models.entities;


public class Cliente {

   
  private int id;
    private String cliente;


    public Cliente() {
    }

    public Cliente(int id, String cliente) {
        this.id = id;
        this.cliente = cliente;
    }

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCliente() {
        return this.cliente;
    }
    
    
    
    
    
    
    
}
